def soma(a,b):
    try:
     return float(a) + float(b)
    except:
     return None
def sub(a,b):
    try:
     return float(a) - float(b)
    except:
     return None
def multi(a,b):
    try:
     return float(a) * float(b)
    except:
     return None
def div(a,b):
    try:
     return float(a) / float(b)
    except:
     return None
 
